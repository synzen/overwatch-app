import 'package:flutter/material.dart';
import 'package:overwatchapp/routes_list.dart';
import 'package:overwatchapp/saved_route_stop.dart';

void main() => runApp(const MyApp());

class MyApp extends StatelessWidget {
  const MyApp({super.key});

  @override
  Widget build(BuildContext context) {
    const String appTitle = 'Overwatch';
    return MaterialApp(
      title: appTitle,
      home: Scaffold(
        appBar: AppBar(
          title: const Text(appTitle),
        ),
        floatingActionButton: const AddStopButton(),
        body: const SingleChildScrollView(
          child: Column(
            children: [
              SavedRoutesList(),
              TitleSection(
                name: 'Name',
                location: 'Location',
              ),
              FavoriteWidget(),
            ],
          ),
        ),
      ),
    );
  }
}

class SavedRoutesList extends StatelessWidget {
  const SavedRoutesList({super.key});

  @override
  Widget build(BuildContext context) {
    return const Column(
      children: [
        SavedRouteStop(
          name: "Stop 1",
          stopId: "MTA_301986",
        ),
      ],
    );
  }
}

class AddStopButton extends StatelessWidget {
  const AddStopButton({super.key});

  @override
  Widget build(BuildContext context) {
    return FloatingActionButton(
      onPressed: () {
        Navigator.push(
          context,
          MaterialPageRoute(builder: (context) => const RoutesList()),
        );
      },
      child: const Icon(Icons.add),
    );
  }
}

